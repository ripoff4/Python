# Mathematical Operators And Comparative Operators
import math
a, b = 10, 5
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a % b)

print(a > b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

# String Operators
str1, str2 = "Hello", "World"
print(str1+" "+str2)

# .math Operators
x, y, z = 10, 2, 5.7
print(math.pow(x, y))
print(math.sqrt(x))
print(math.ceil(z))
print(math.floor(z))
